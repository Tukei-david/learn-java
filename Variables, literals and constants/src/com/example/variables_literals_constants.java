package com.example;

public class variables_literals_constants
{
    public static void main(String[] args)
    {
        int firstNumber;
        int secondNumber;

        firstNumber = 11;
        secondNumber = 20;

        double average;

        average = (firstNumber + secondNumber) / 2.0;

        System.out.print("Average: " + average);

        String name;
        String surname;

        name = "John";
        surname = "Rambo";

        System.out.print(name + " " + surname);

        boolean value;
        value = false;

        System.out.print(value);

        char letter;
        letter = 'j';

        System.out.print(letter);
    }
}
